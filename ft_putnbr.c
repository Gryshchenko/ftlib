#include "libft.h"

void	ft_putnbr(int n)
{
	long	nb;

	nb = (long)n;
	if (nb > 2147483647 || nb < -2147483648)
		return ;
	if (nb < 0)
	{
		ft_putchar('-');
		nb = -nb;
	}
	if (nb > 9)
	{
		ft_putnbr(nb / 10);
		ft_putnbr(nb % 10);
	}
	else
		ft_putchar(nb + '0');
}
